import { createEffect, createSignal } from "solid-js";

export default function MenuButton({ onToggle }) {
  const [on, setOn] = createSignal(false);

  createEffect(() => {
    if (typeof onToggle === "function") {
      onToggle(on());
    }
    console.log("The menu button is", on());
  }, on());

  return (
    <button
      data-collapse-toggle="navbar-default"
      type="button"
      class="md:hidden items-center p-4 ml-3 text-sm text-gray-500 rounded-lg hover:bg-gray-100 focus:outline-none focus:ring-2 focus:ring-gray-200 dark:text-gray-400 dark:hover:bg-gray-700 dark:focus:ring-gray-600"
      aria-controls="navbar-default"
      aria-expanded="false"
      onClick={() => setOn(!on())}
    >
      <span class="sr-only">Open main menu</span>
      <svg
        class="w-6 h-6"
        aria-hidden="true"
        fill="currentColor"
        viewBox="0 0 20 20"
        xmlns="http://www.w3.org/2000/svg"
      >
        <path
          fill-rule="evenodd"
          d="M3 5a1 1 0 011-1h12a1 1 0 110 2H4a1 1 0 01-1-1zM3 10a1 1 0 011-1h12a1 1 0 110 2H4a1 1 0 01-1-1zM3 15a1 1 0 011-1h12a1 1 0 110 2H4a1 1 0 01-1-1z"
          clip-rule="evenodd"
        ></path>
      </svg>
    </button>
  );
}
